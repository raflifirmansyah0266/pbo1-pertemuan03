package operator;

public class Switch {
    public static void main(String[] args){
        int jalanjalan=1;
        switch (jalanjalan){
            case 1:
                System.out.println("urang lapas");
                break;
            case 2:
                System.out.println("urang acayan");
                break;
            case 3:
                System.out.println("ubat nyamuk");
                break;
            case 4:
                System.out.println("dubil dit");
            default:
                System.out.println("mahkluk halus");
                break;
        }

    }
}
